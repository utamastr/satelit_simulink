function [qdot] = w2qdot (in);

        % -------------------------  implementation   -----------------
        r=[0 0 0];
        v=[0 0 0];
        
        q0=in(1);
        q1=in(2);
        q2=in(3);
        q3=in(4);
        w1=in(5);
        w2=in(6);
        w3=in(7);
        
        A=[-q1 -q2 -q3;
            q0 -q3 q2;
            q3 q0 -q1;
            -q2 q1 q0];
        
        w=[w1;w2;w3];
        
        qdot=0.5*A*w;
                
        
       
        
